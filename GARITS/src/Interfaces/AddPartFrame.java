    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:

        if (!(this.numberTextField.getText().length() > 0)) {
            this.messageLabel.setForeground(Color.red);
            this.messageLabel.setText("Please enter a part number.");
            return;
        }

        this.messageLabel.setForeground(Color.black);

        part.setNumber(this.numberTextField.getText());
        part.setName(this.nameTextField.getText());
        part.setDescription(this.descriptionTextField.getText());
        part.setManufacturer(this.manufacturerTextField.getText());
        part.setVehicleType(this.vehicleTypeTextField.getText());
        part.setYears(this.yearsTextField.getText());
        part.setThreshold(Integer.parseInt("" + this.thresholdTextField.getText()));

        if (edit) {

            controller.updateItem(part);

        } else {

            stock.setPart(part);
            stock.setItemNumber(part.number());
            stock.setQuantity(Integer.parseInt(this.stockLevelTextField.getText()));
            stock.setCost(Double.parseDouble("" + this.costTextField.getValue()));

            if (controller.addItem(stock)) {

                this.messageLabel.setForeground(Color.black);
                this.messageLabel.setText("Successfully added part.");
                this.delegate.reset();
            } else {
                this.messageLabel.setForeground(Color.red);
                this.messageLabel.setText("Another part with the same number already exists.");
            }
        }


    }//GEN-LAST:event_addButtonActionPerformed

