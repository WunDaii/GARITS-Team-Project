    @Override
    public void editItem(Item item) {
        if (item.type() == ItemType.PART) {
            editPart((Part) item);
        } else if (item.type() == ItemType.STOCK) {
            editStock((Stock) item);
        }
    }

    @Override
    public void selectedItems(ArrayList<? extends Item> items) {
        System.out.println("selectedItems: " + items.size());
        Item first = items.get(0);

        switch (first.type()) {
            case SUPPLIER:

                Supplier supplier = (Supplier) first;
                part.setSupplier(supplier);
                supplierTextLabel.setText(supplier.name());
                break;
            default:
                break;
        }
    }

