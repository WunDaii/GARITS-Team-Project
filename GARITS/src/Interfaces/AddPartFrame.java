    public void editPart(Part part) {
        this.numberTextField.setText(part.number());
        this.numberTextField.setEnabled(false);
        this.nameTextField.setText(part.name());
        this.descriptionTextField.setText(part.description());
        this.manufacturerTextField.setText(part.manufacturer());
        this.vehicleTypeTextField.setText(part.vehicleType());
        this.yearsTextField.setText(part.years());
        this.part = part;
        edit = true;
        this.titleLabel.setText("Edit Part");
        this.addButton.setText("Update Part");
        supplierTextLabel.setText(part.supplier().name());
        this.thresholdTextField.setValue(part.threshold());
    }

