    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:

        supplier.setName(nameTextField.getText());
        supplier.setAddress(addressTextField.getText());
        supplier.setPostCode(postcodeTextField.getText());

        if (edit) {
            controller.updateItem(supplier);
        } else if (controller.addItem(supplier)) {
            messageLabel.setForeground(Color.black);
            messageLabel.setText("Successfully added supplier.");
        } else {
            messageLabel.setForeground(Color.red);
            messageLabel.setText("Supplier '" + supplier.name() + "' already exists.");
        }
    }//GEN-LAST:event_addButtonActionPerformed

