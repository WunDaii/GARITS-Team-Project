    public void editSupplier(Supplier supplier_) {

        edit = true;
        supplier = supplier_;

        this.nameTextField.setText(supplier.name());
        addressTextField.setText(supplier.address());
        postcodeTextField.setText(supplier.postcode());
        supplier.setParts(controller.getPartsForSupplier(supplier));
        refresh();

        titleLabel.setText("Edit Supplier");
        addButton.setText("Update Supplier");
    }

    public void refresh() {
        model.setItems(supplier.parts());
        partsTable.setModel(model);
        partsTable.revalidate();
    }

