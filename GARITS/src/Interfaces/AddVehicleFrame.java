    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:

        if (this.vehicle.customer() == null && customerTextField.getText().equals("")) {
            setError("Please select a customer.");
            return;
        }

        this.vehicle.setRegNo(regNoTextField.getText());
        if (this.MOTTextField.getText().length() > 9) {
            try {
                SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
                java.util.Date parsed = format.parse(this.MOTTextField.getText());
                java.sql.Date sqlDate = new java.sql.Date(parsed.getTime());
                vehicle.setMOTDate(sqlDate);
            } catch (ParseException ex) {
            }
        }

        vehicle.setMake(makeTextField.getText());
        vehicle.setModel(modelTextField.getText());
        vehicle.setEngSerial(engineTextField.getText());
        vehicle.setChassisNumber(chassisTextField.getText());
        vehicle.setColour(colourTextField.getText());
        vehicle.setYear(Integer.parseInt(yearTextField.getText()));

        if (edit) {
            controller.updateItem(vehicle);
            setSuccess("Successfully updated vehicle.");
        } else if (controller.addItem(vehicle)) {
            setSuccess("Successfully added new vehicle.");
        } else {
            setError("Vehicle with this registration number already exists.");
        }

        delegate.reset();
    }//GEN-LAST:event_addButtonActionPerformed

