    public void editVehicle(Vehicle vehicle_) {

        this.vehicle = vehicle_;
        edit = true;
        titleLabel.setText("Edit Vehicle");
        this.addButton.setText("Update Vehicle");

        if (this.vehicle.MOTDate() != null) {
            this.MOTTextField.setText(new SimpleDateFormat("dd/MM/yyyy").format(this.vehicle.MOTDate()));
        }

        this.regNoTextField.setText(vehicle.regNo());
        makeTextField.setText(vehicle.make());
        modelTextField.setText(vehicle.model());
        yearTextField.setText("" + vehicle.year());
        engineTextField.setText(vehicle.engSerial());
        chassisTextField.setText(vehicle.chassisNumber());
        colourTextField.setText(vehicle.colour());
        customerTextField.setText(vehicle.customer().firstName() + " " + vehicle.customer().lastName());
    }

